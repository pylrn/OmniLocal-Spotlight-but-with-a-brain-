#[derive(Debug, Clone)]
pub struct SentenceSpan {
    pub text: String,
    pub start_line: usize,
    pub end_line: usize,
    pub start_offset: usize,
    pub end_offset: usize,
}

/// Compute cosine similarity between two vectors
fn cosine_similarity(a: &[f32], b: &[f32]) -> f64 {
    let dot: f64 = a.iter().zip(b.iter()).map(|(x, y)| (*x as f64) * (*y as f64)).sum();
    let norm_a: f64 = a.iter().map(|x| (*x as f64) * (*x as f64)).sum::<f64>().sqrt();
    let norm_b: f64 = b.iter().map(|x| (*x as f64) * (*x as f64)).sum::<f64>().sqrt();

    if norm_a == 0.0 || norm_b == 0.0 {
        return 0.0;
    }

    dot / (norm_a * norm_b)
}

/// Find indices where the similarity drops below the percentile threshold
fn detect_boundaries(similarities: &[f64], percentile: f64) -> Vec<usize> {
    if similarities.is_empty() {
        return vec![];
    }

    // Calculate distances (1 - similarity)
    let distances: Vec<f64> = similarities.iter().map(|s| 1.0 - s).collect();

    // Find the percentile threshold
    let mut sorted = distances.clone();
    sorted.sort_by(|a, b| a.partial_cmp(b).unwrap_or(std::cmp::Ordering::Equal));

    let percentile_idx = ((percentile * sorted.len() as f64) as usize).min(sorted.len() - 1);
    let threshold = sorted[percentile_idx];

    debug!("Boundary threshold ({}th percentile): {:.4}", (percentile * 100.0) as usize, threshold);

    // Find boundaries where distance exceeds threshold
    distances
        .iter()
        .enumerate()
        .filter(|(_, &d)| d >= threshold && d > 0.1) // Also require minimum absolute distance
        .map(|(i, _)| i)
        .collect()
}

/// Form a chunk from a range of sentences
fn form_chunk(
    sentences: &[SentenceSpan],
    start: usize,
    end: usize,
    chunk_index: usize,
    config: &ChunkerConfig,
) -> TextChunk {
    let content: String = sentences[start..=end]
        .iter()
        .map(|s| s.text.as_str())
        .collect::<Vec<_>>()
        .join(" ");

    TextChunk {
        content: content.clone(),
        chunk_index,
        start_line: sentences[start].start_line,
        end_line: sentences[end].end_line,
        estimated_tokens: estimate_tokens(&content, config.tokens_per_word),
    }
}

/// Post-process chunks: merge tiny chunks, split oversized ones
fn post_process_chunks(mut chunks: Vec<TextChunk>, config: &ChunkerConfig) -> Vec<TextChunk> {
    // Merge chunks that are too small
    let mut merged: Vec<TextChunk> = Vec::new();

    for chunk in chunks.drain(..) {
        if let Some(last) = merged.last_mut() {
            if last.estimated_tokens < config.min_chunk_tokens
                || chunk.estimated_tokens < config.min_chunk_tokens
            {
                // Merge with previous
                last.content = format!("{} {}", last.content, chunk.content);
                last.end_line = chunk.end_line;
                last.estimated_tokens = estimate_tokens(&last.content, config.tokens_per_word);
                continue;
            }
        }
        merged.push(chunk);
    }

    // Re-index chunks
    for (i, chunk) in merged.iter_mut().enumerate() {
        chunk.chunk_index = i;
    }

    // Split chunks that are too large (fallback: split at midpoint)
    let mut final_chunks: Vec<TextChunk> = Vec::new();
    for chunk in merged {
        if chunk.estimated_tokens > config.max_chunk_tokens {
            // Split at sentence boundaries
            let sub_sentences = segment_sentences(&chunk.content);
            let mid = sub_sentences.len() / 2;

            if mid > 0 && sub_sentences.len() > 1 {
                let first_half: String = sub_sentences[..mid]
                    .iter()
                    .map(|s| s.text.as_str())
                    .collect::<Vec<_>>()
                    .join(" ");
                let second_half: String = sub_sentences[mid..]
                    .iter()
                    .map(|s| s.text.as_str())
                    .collect::<Vec<_>>()
                    .join(" ");

                final_chunks.push(TextChunk {
                    content: first_half.clone(),
                    chunk_index: final_chunks.len(),
                    start_line: chunk.start_line,
                    end_line: sub_sentences[mid - 1].end_line + chunk.start_line - 1,
                    estimated_tokens: estimate_tokens(&first_half, config.tokens_per_word),
                });
                final_chunks.push(TextChunk {
                    content: second_half.clone(),
                    chunk_index: final_chunks.len(),
                    start_line: sub_sentences[mid].start_line + chunk.start_line - 1,
                    end_line: chunk.end_line,
                    estimated_tokens: estimate_tokens(&second_half, config.tokens_per_word),
                });
            } else {
                final_chunks.push(TextChunk {
                    chunk_index: final_chunks.len(),
                    ..chunk
                });
            }
        } else {
            final_chunks.push(TextChunk {
                chunk_index: final_chunks.len(),
                ..chunk
            });
        }
    }

    final_chunks
}

/// Rough token count estimation (word count × tokens-per-word ratio)
fn estimate_tokens(text: &str, tokens_per_word: f64) -> usize {
    let word_count = text.split_whitespace().count();
    (word_count as f64 * tokens_per_word) as usize
}

