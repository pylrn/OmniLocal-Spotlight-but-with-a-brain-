// SmartSearch — Semantic Boundary Chunker
// Splits documents into coherent chunks based on topic transitions,
// not arbitrary token limits.

use unicode_segmentation::UnicodeSegmentation;
use log::debug;

/// Configuration for the semantic chunker
#[derive(Debug, Clone)]
pub struct ChunkerConfig {
    /// Maximum tokens per chunk (fallback split if exceeded)
    pub max_chunk_tokens: usize,
    /// Minimum tokens per chunk (merge with adjacent if below)
    pub min_chunk_tokens: usize,
    /// Percentile threshold for boundary detection (0.0 - 1.0)
    /// Higher = more aggressive splitting
    pub boundary_percentile: f64,
    /// Approximate tokens-per-word ratio for rough estimation
    pub tokens_per_word: f64,
}

impl Default for ChunkerConfig {
    fn default() -> Self {
        Self {
            max_chunk_tokens: 1500,
            min_chunk_tokens: 50,
            boundary_percentile: 0.90,
            tokens_per_word: 1.3,
        }
    }
}

/// A text chunk with metadata
#[derive(Debug, Clone, serde::Serialize)]
pub struct TextChunk {
    pub content: String,
    pub chunk_index: usize,
    pub start_line: usize,
    pub end_line: usize,
    pub estimated_tokens: usize,
}

/// Segment text into sentences using Unicode-aware segmentation
pub fn segment_sentences(text: &str) -> Vec<SentenceSpan> {
    let mut sentences = Vec::new();
    let mut current_line = 1;
    let mut pos = 0;

    // Use unicode_segmentation for sentence boundaries
    for sentence in text.split_sentence_bounds() {
        let trimmed = sentence.trim();
        if trimmed.is_empty() {
            // Count newlines in whitespace
            current_line += sentence.matches('\n').count();
            pos += sentence.len();
            continue;
        }

        let start_line = current_line;
        current_line += sentence.matches('\n').count();
        let end_line = current_line;

        sentences.push(SentenceSpan {
            text: trimmed.to_string(),
            start_line,
            end_line,
            start_offset: pos,
            end_offset: pos + sentence.len(),
        });

        pos += sentence.len();
    }

    sentences
}

/// Perform semantic chunking using embedding-based boundary detection
///
/// The `embeddings` parameter contains one embedding vector per sentence,
/// generated by the AI provider in a batch call.
pub fn semantic_chunk(
    sentences: &[SentenceSpan],
    embeddings: &[Vec<f32>],
    config: &ChunkerConfig,
) -> Vec<TextChunk> {
    if sentences.is_empty() {
        return vec![];
    }

    if sentences.len() == 1 {
        return vec![TextChunk {
            content: sentences[0].text.clone(),
            chunk_index: 0,
            start_line: sentences[0].start_line,
            end_line: sentences[0].end_line,
            estimated_tokens: estimate_tokens(&sentences[0].text, config.tokens_per_word),
        }];
    }

    // Step 1: Calculate cosine similarity between adjacent sentences
    let mut similarities: Vec<f64> = Vec::with_capacity(sentences.len() - 1);
    for i in 0..embeddings.len() - 1 {
        let sim = cosine_similarity(&embeddings[i], &embeddings[i + 1]);
        similarities.push(sim);
    }

    debug!("Cosine similarities between adjacent sentences: {:?}", similarities);

    // Step 2: Find boundary points using percentile threshold
    let boundaries = detect_boundaries(&similarities, config.boundary_percentile);

    debug!("Detected boundaries at sentence indices: {:?}", boundaries);

    // Step 3: Form chunks from boundary points
    let mut chunks = Vec::new();
    let mut chunk_start = 0;

    for &boundary in &boundaries {
        let chunk = form_chunk(sentences, chunk_start, boundary, chunks.len(), config);
        chunks.push(chunk);
        chunk_start = boundary + 1;
    }

    // Add final chunk
    if chunk_start < sentences.len() {
        let chunk = form_chunk(sentences, chunk_start, sentences.len() - 1, chunks.len(), config);
        chunks.push(chunk);
    }

    // Step 4: Post-process — merge tiny chunks, split oversized ones
    post_process_chunks(chunks, config)
}

/// Fallback chunking: fixed-size with overlap (used when no embeddings available)
pub fn fixed_size_chunk(text: &str, config: &ChunkerConfig) -> Vec<TextChunk> {
    let sentences = segment_sentences(text);
    if sentences.is_empty() {
        return vec![];
    }

    let mut chunks = Vec::new();
    let mut current_chunk_sentences: Vec<&SentenceSpan> = Vec::new();
    let mut current_tokens = 0;

    for sentence in &sentences {
        let sentence_tokens = estimate_tokens(&sentence.text, config.tokens_per_word);

        if current_tokens + sentence_tokens > config.max_chunk_tokens && !current_chunk_sentences.is_empty() {
            // Emit current chunk
            let content: String = current_chunk_sentences
                .iter()
                .map(|s| s.text.as_str())
                .collect::<Vec<_>>()
                .join(" ");

            chunks.push(TextChunk {
                content,
                chunk_index: chunks.len(),
                start_line: current_chunk_sentences.first().unwrap().start_line,
                end_line: current_chunk_sentences.last().unwrap().end_line,
                estimated_tokens: current_tokens,
            });

            current_chunk_sentences.clear();
            current_tokens = 0;
        }

        current_chunk_sentences.push(sentence);
        current_tokens += sentence_tokens;
    }

    // Emit remaining
    if !current_chunk_sentences.is_empty() {
        let content: String = current_chunk_sentences
            .iter()
            .map(|s| s.text.as_str())
            .collect::<Vec<_>>()
            .join(" ");

        chunks.push(TextChunk {
            content,
            chunk_index: chunks.len(),
            start_line: current_chunk_sentences.first().unwrap().start_line,
            end_line: current_chunk_sentences.last().unwrap().end_line,
            estimated_tokens: current_tokens,
        });
    }

    chunks
}

